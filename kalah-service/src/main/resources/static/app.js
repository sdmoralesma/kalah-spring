class AirSlot extends HTMLElement {

  constructor() {
    super();
    this.oldChild = null;
    this.currentView = null;
    this.root = this.attachShadow({mode: 'open'});
  }

  connectedCallback() {
    this.root.innerHTML = `
      <style>
      slot[name="view"] {
        display: flex;
        align-items: center;
        justify-content: center;
        width: 100%;
        height: 100%;
      }
      </style>
      <slot name="view">VIEW</slot>
    `;
    document.addEventListener('air-nav', e => this.onNavigation(e));
    this.oldChild = this.root.querySelector("[name=view]");
  }

  onNavigation(evt) {
    const {detail} = evt;
    const {hash: linkName} = detail;
    this.currentView = linkName;
    this.loadView(linkName);
  }

  async loadView(linkName) {
    let newChild;
    switch (linkName) {
      case 'About':
        newChild = new AboutView();
        break;
      case 'KalahBoard':
        newChild = new KalahBoard();
        break;
      case 'Overview':
        newChild = new OverviewView();
        break;
      default:
        throw new Error(`Unknown route: ${linkName}`);
    }

    if (this.oldChild) {
      this.root.replaceChild(newChild, this.oldChild);
    } else {
      this.root.appendChild(newChild);
    }

    this.oldChild = newChild;
  }

}

customElements.define('air-slot', AirSlot);

class AirCrumb extends HTMLElement {

  constructor() {
    super();
  }

  connectedCallback(){
    document.addEventListener('air-nav', e => this.onNavigation(e));
  }

  onNavigation({detail}) {
    const {hash} = detail;
    this.displayCurrent(hash);
  }

  displayCurrent(linkName){
    this.innerText = `>${linkName}`;
  }
}

customElements.define('air-crumb', AirCrumb);

class AirUpdate extends HTMLElement {

  constructor() {
    super();
  }

  connectedCallback() {
    render(this.createView(), this);
  }

  createView() {
    return html`
      <a href="#" @click=${e => this.update(e)}>update</a>
    `;
  }

  update(event) {
    event.preventDefault();
    console.log('---updating---');
    navigator.serviceWorker.controller.postMessage('update.please');
  }
}

customElements.define('air-update', AirUpdate);

